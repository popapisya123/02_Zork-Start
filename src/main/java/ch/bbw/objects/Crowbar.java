package ch.bbw.objects;

public class Crowbar {

  private int id;
  private double weight;

  public Crowbar(int id, double weight) {
    this.id = id;
    this.weight = weight;
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public double getWeight() {
    return weight;
  }

  public void setWeight(double weight) {
    this.weight = weight;
  }
}
